    private MaskFormatter mCPF, mTel;
    
    private void definirMascara() {
        try {
            mCPF = new MaskFormatter();
            mTel = new MaskFormatter();
            
            mCPF.setMask("###.###.###-##"); 
            mCPF.setPlaceholderCharacter('_');
            
            mTel.setMask("(##) ####-####");
            mTel.setPlaceholderCharacter('_');
            
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
    }
