    private void jfCpfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jfCpfFocusLost
        
        if(!jfCpf.getText().contains("_")){
            if(!validaCPF(jfCpf.getText())){
                jfCpf.setForeground(Color.RED);
                JOptionPane.showMessageDialog(null, "Error - Invalid CPF", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                jfCpf.setForeground(Color.BLUE);
            }
        }
        
    }//GEN-LAST:event_jfCpfFocusLost

    private boolean validaCPF(String cpf){
        return cpf.equals("111.111.111-11");
    }
    
